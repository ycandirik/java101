
public class whileDongusu {
    public static void main(String[] args) {
        int i = 1;
        int k;

        while (i <= 5){
            System.out.println(i);
            i++;
            k = 1;
            while(k <= 10) {
                System.out.print(k + " , ");
                k++;
            }
            System.out.println();
        }



    }
}
